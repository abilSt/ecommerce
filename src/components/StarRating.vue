<template>
  <div class="star-rating">
    <div class="star-rating__stars">
      <span v-for="star in starLimit" :key="star">
        <base-icon fill="lightgray">
          <star-icon></star-icon>
        </base-icon>
      </span>

      <div class="star-rating__contained">
        <span v-for="star in starLimit" :key="star">
          <base-icon fill="#ffa801">
            <star-icon></star-icon>
          </base-icon>
        </span>
      </div>
    </div>

    <span class="star-rating__number">{{ rating }}</span>
  </div>
</template>

<script>
import BaseIcon from "./UI/BaseIcon.vue";
import StarIcon from "@/components/icons/StarIcon.vue";
export default {
  components: { BaseIcon, StarIcon },
  props: {
    rating: {
      type: Number,
      required: true,
    },
    starLimit: {
      type: Number,
      default: 5,
    },
  },

  computed: {
    ratingWidth() {
      return (this.rating * 100) / this.starLimit;
    },
  },
};
</script>

<style scoped>
.star-rating {
  display: flex;
  gap: 15px;
}

.star-rating__stars {
  position: relative;
}

.star-rating__contained {
  display: inline-flex;
  position: absolute;
  top: 0;
  left: 0;
  overflow: hidden;
  width: v-bind(ratingWidth + "%");
}

.star-rating__number {
  font-weight: 600;
}
</style>
